//encapsulation //data wrapping i.e data and data related methods
                 

package java_package;


public class java_class
{
  private int Rollno;      //instance /non-static variable
  private String Name;
  
  public void setRollno(int x)  //setter method
  {
	  Rollno=x;
  }
  public int getRollno()       //getter method
  {
	  return Rollno;
  }

  public void setName(String y)
  {
	Name=y;
  }
  
  public String getName()
  {
	  return Name;
  }

}




